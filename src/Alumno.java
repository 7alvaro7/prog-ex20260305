public class Alumno {


    private int id;
    private String nombre;
    private String tituloFP;
    private int edad;


    public Alumno(int id, String nombre, String tituloFP, int edad) {

        //  Aqui se guarda los valores recibidos
        this.id = id;
        this.nombre = nombre;
        this.tituloFP = tituloFP;
        this.edad = edad;
    }

}